USE 2255_MGIS445_4;
SET FOREIGN_KEY_CHECKS=0;

-- SHIRT ID FORMAT REFERENCE: AABBCC
-- AA = Size: 00=2XS, 01=XS, 02=S, 03=M, 04=L, 05=XL, 07=2XL, 08=3XL
-- BB = Color: 00=Blue, 01=Green, 02=Pink, 03=Red
-- CC = Design: 00-99
-- Examples: 000000 (2XS Blue), 030000 (M Blue), 040102 (L Red)

-- TABLE 0: AddressBook (if not exists)
CREATE TABLE IF NOT EXISTS AddBook (
    addUUID INT AUTO_INCREMENT PRIMARY KEY,
    AddressLine1 VARCHAR(255),
    AddressLine2 VARCHAR(255),
    City VARCHAR(100),
    State CHAR(2),
    ZipCode INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- TABLE 0: CustomerInfo (if not exists)
CREATE TABLE IF NOT EXISTS CustInfo (
    CusUUID INT AUTO_INCREMENT PRIMARY KEY,
    FirstName VARCHAR(50),
    LastName VARCHAR(50),
    MI CHAR(1),
    dob DATE,
    billingAdd INT,
    shippingAdd INT,
    emailAddress VARCHAR(100),
    Password VARCHAR(255),
    phoneNumber BIGINT,
    Username VARCHAR(100) UNIQUE,
    FOREIGN KEY (billingAdd) REFERENCES AddBook(addUUID),
    FOREIGN KEY (shippingAdd) REFERENCES AddBook(addUUID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- TABLE 1: Shirts (Product Catalog)
CREATE TABLE IF NOT EXISTS Shirts (
    ShirtID VARCHAR(6) PRIMARY KEY COMMENT 'Format: AABBCC (Size|Color|Design)',
    SizeName VARCHAR(10) NOT NULL,
    ColorName VARCHAR(50) NOT NULL,
    DesignName VARCHAR(100) NOT NULL,
    Price DECIMAL(10, 2) NOT NULL,
    Stock INT DEFAULT 0,
    INDEX idx_size (SizeName),
    INDEX idx_color (ColorName),
    INDEX idx_design (DesignName)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- TABLE 2: ShoppingCart (Temporary Cart Storage)
CREATE TABLE IF NOT EXISTS ShoppingCart (
    CartID INT AUTO_INCREMENT PRIMARY KEY,
    CusUUID INT NOT NULL,
    ShirtID VARCHAR(6) NOT NULL,
    Quantity INT DEFAULT 1,
    AddedDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (CusUUID) REFERENCES CustInfo(CusUUID) ON DELETE CASCADE,
    FOREIGN KEY (ShirtID) REFERENCES Shirts(ShirtID) ON DELETE CASCADE,
    INDEX idx_customer (CusUUID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- TABLE 3: OrderHistory (Customer Orders)
CREATE TABLE IF NOT EXISTS OrderHistory (
    OrderUUID INT AUTO_INCREMENT PRIMARY KEY,
    CusUUID INT NOT NULL,
    OrderDateTime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    TotalPrice DECIMAL(10, 2) NOT NULL,
    OrderStatus ENUM('pending', 'confirmed', 'shipped', 'delivered', 'cancelled') DEFAULT 'confirmed',
    ShippingAddID INT,
    BillingAddID INT,
    FOREIGN KEY (CusUUID) REFERENCES CustInfo(CusUUID) ON DELETE CASCADE,
    FOREIGN KEY (ShippingAddID) REFERENCES AddBook(addUUID) ON DELETE SET NULL,
    FOREIGN KEY (BillingAddID) REFERENCES AddBook(addUUID) ON DELETE SET NULL,
    INDEX idx_customer (CusUUID),
    INDEX idx_order_date (OrderDateTime)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- TABLE 4: OrderItems (Individual Items in Orders)
CREATE TABLE IF NOT EXISTS OrderItems (
    OrderItemID INT AUTO_INCREMENT PRIMARY KEY,
    OrderUUID INT NOT NULL,
    ShirtID VARCHAR(6) NOT NULL,
    Quantity INT NOT NULL,
    PricePerUnit DECIMAL(10, 2) NOT NULL,
    Subtotal DECIMAL(10, 2) NOT NULL,
    FOREIGN KEY (OrderUUID) REFERENCES OrderHistory(OrderUUID) ON DELETE CASCADE,
    FOREIGN KEY (ShirtID) REFERENCES Shirts(ShirtID) ON DELETE RESTRICT,
    INDEX idx_order (OrderUUID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

